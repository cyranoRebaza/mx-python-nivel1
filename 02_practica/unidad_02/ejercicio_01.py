"""
Unidad 02 - Ejercicio 01

Hacer un programa para solicitar por teclado dos números y luego mostrar la suma
de los mismos.

"""
# Pedir datos
numero1 = int(input("Ingrese el primer número: "))
numero2 = int(input("Ingrese el segundo número: "))

# Calcular la suma
resultado = numero1 + numero2

# Mostrar resultado
print(f"El resultado de la suma es: {resultado}")
