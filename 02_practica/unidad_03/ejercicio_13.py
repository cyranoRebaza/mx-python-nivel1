"""
Unidad 03 - Ejercicio 13

Hacer un programa que solicite el ingreso de un número y luego emita un cartel
por pantalla aclarando si el mismo es múltiplo de 5.

"""
# Pedir datos
numero = int(input("Ingrese un numero: "))

# Calcular y mostrar
if numero % 5 == 0:
    print(f"El numero {numero} es multiplo de 5")
    
else:
    print(f"El numero {numero} no es multiplo de 5")


    



    