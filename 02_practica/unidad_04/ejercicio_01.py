"""
Unidad 04 - Ejercicio 01

Hacer un programa para mostrar los números del 1 al 10. No se debe realizar 
ningún pedido de datos. 

Nota: probalo tanto con For como con While.

"""

# usando for
for numero in range(1, 11):
    print(numero)

print()
# usando while
numero_2 = 1
while numero_2 <= 10:
    print(numero_2)
    numero_2 += 1
